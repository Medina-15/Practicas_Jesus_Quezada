    private void btnDivicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDivicionActionPerformed
                                              
    String t1 = txtValor1.getText();
    String t2 = txtValor2.getText();
    
    if(t1.equals("") || t2.equals("")) {
        JOptionPane.showMessageDialog(null, "Faltan datos");
        return;
    }
    
    try {
        double v1 = Double.parseDouble(t1);
        double v2 = Double.parseDouble(t2);
        
        
        if (v2 == 0) {
            JOptionPane.showMessageDialog(null, "Error: No se puede dividir por cero", "Error Matemático", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        txtRespuesta.setText("" + (v1 / v2));
    } catch(Exception e) {
        JOptionPane.showMessageDialog(null, "Solo números por favor");
    }

    }//GEN-LAST:event_btnDivicionActionPerformed

    private void btnRaizCuadradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRaizCuadradaActionPerformed
         String t1 = txtValor1.getText();
    
        if(t1.equals("")) {
            JOptionPane.showMessageDialog(null, "Falta el número para la raíz");
            return;
        }
    
        try {
            double v1 = Double.parseDouble(t1);
            
            if (v1 < 0) {
                JOptionPane.showMessageDialog  (null, "Error: No existe raíz de negativo", "Error Matemático", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            txtRespuesta.setText("" + Math.sqrt(v1));
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Solo números por favor");
        }
                
    
    }//GEN-LAST:event_btnRaizCuadradaActionPerformed

