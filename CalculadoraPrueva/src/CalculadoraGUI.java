    private void btnTemperaturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTemperaturaActionPerformed
     String t1 = txtValor1.getText();
        
        if(t1.equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese la temperatura en Valor 1");
            return;
        }
        
        try {
            double fahrenheit = Double.parseDouble(t1);
            double celsius = (fahrenheit - 32) * 5/9.0;
            txtResultadoCelsius.setText(String.format("%.2f °C", celsius));
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Solo números por favor");
        }
                                                       
   
    }//GEN-LAST:event_btnTemperaturaActionPerformed

